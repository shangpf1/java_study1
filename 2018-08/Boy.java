package lianxi;

public class Boy {
	
	//属性
	private String name;
	private String sex;
	private int age;
	private String addr;
	
	//方法
	public String getName(){
		return name;
	}
	
	public void setName(String name){
		this.name=name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}
    
}
